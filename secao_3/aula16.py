"""
   Operadores de comparação (Relacionais)
   
OP          Significado         Exemplo
>           Maior que           2 > 1      #True
>=          Maior ou Igual      2 >= 2     #True
<           Menor que           1 < 2      #True
<=          Menor ou Igual      2 <= 2     #True
==          Igual               'a' == 'a' #True
!=          Diferente           'a' != 'b' #True
"""

maior = 2 > 1
maior_ou_igual = 2 >= 2
menor = 1 < 2
menor_ou_igual = 2 <= 2
igual = 'a' == 'a'
diferente = 'a' != 'b'


print(maior)
print(maior_ou_igual)
print(menor)
print(menor_ou_igual)
print(igual)
print(diferente)
